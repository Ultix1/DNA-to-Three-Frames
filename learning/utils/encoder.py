def get_protein_encoding(proteins : list = proteins):
    return pd.get_dummies(proteins, dtype=int).to_dict(orient='list')

def get_codon_encoding(proteins : dict, codons : dict = genetic_code):
    new_codons = codons.copy()
    for key, value in new_codons.items():
        new_codons[key] = proteins[value]

    return new_codons

def get_table():
    return genetic_code

